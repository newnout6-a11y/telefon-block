def write_spam_numbers(rows: List[Dict[str, str]]) -> int:
    block_numbers = sorted({
        r['номер'] for r in rows
        if r.get('метка') == 'BLOCK' and (r.get('номер') or '').startswith('+')
    })
    header = [
        '# Auto-generated bundle of known spam numbers (Russia).',
        '# Source: datasets/ru/processed/ru_numbers_labeled.csv',
        f'# Generated: {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}',
        '# Format:',
        '#   exact:    +74951234567',
        '#   prefix:   prefix:+7800',
        '#   regex:    regex:8800.*',
        '# DO NOT edit this file by hand — regenerate via scripts/build_assets_from_dataset.py.',
        '',
        '# --- Curated risky prefixes (toll-free, Moscow, common spam mobile codes) ---',
    ]
    body_prefix = [f'prefix:{p}' for p in CURATED_PREFIXES]
    body_exact = block_numbers
    lines = header + body_prefix + ['', f'# --- {len(body_exact)} BLOCK numbers from training dataset ---'] + body_exact
    with open(OUT_CSV, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines) + '\n')
    return len(block_numbers)


def compute_prefix_risk(rows: List[Dict[str, str]]) -> Dict:
    n_total = sum(1 for r in rows if r.get('метка') in ('ALLOW', 'BLOCK', 'WARN'))
    n_block = sum(1 for r in rows if r.get('метка') == 'BLOCK')
    prior = n_block / max(n_total, 1)
    alpha = 10.0

    by_prefix5: Dict[str, Dict[str, int]] = defaultdict(lambda: {'BLOCK': 0, 'TOTAL': 0})
    for r in rows:
        n = r.get('номер') or ''
        lbl = r.get('метка')
        if not n.startswith('+7') or lbl not in ('ALLOW', 'BLOCK', 'WARN'):
            continue
        p = n[:6]  # +7XXXX → 5 digits after country code
        by_prefix5[p]['TOTAL'] += 1
        if lbl == 'BLOCK':
            by_prefix5[p]['BLOCK'] += 1

    risk_by_prefix: Dict[str, float] = {}
    for prefix, c in by_prefix5.items():
        n_t = c['TOTAL']
        n_b = c['BLOCK']
        if n_t < 3:
            continue
        risk = (n_b + alpha * prior) / (n_t + alpha)
        risk_by_prefix[prefix] = round(min(1.0, max(0.0, risk)), 4)

    return {
        'version': datetime.now().strftime('%Y%m%d-%H%M%S'),
        'created_at': datetime.now().isoformat(),
        'source_rows': n_total,
        'overall_block_rate': round(prior, 4),
        'fallback_risk': round(prior, 4),
        'min_samples': 3,
        'smoothing_alpha': alpha,
        'prefix_length': 6,
        'prefixes': dict(sorted(risk_by_prefix.items())),
    }


