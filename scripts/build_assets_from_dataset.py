"""Build runtime assets from the processed training dataset.

Outputs (placed under app/src/main/assets/):
  spam_numbers.csv    — auto-generated bundle of BLOCK-labeled numbers + curated
                        prefix/regex rules. The Android app imports this on first
                        run via CsvSpamImporter.
  prefix_risk.json    — data-driven prefix→risk table (P(BLOCK | prefix-5-digits),
                        smoothed). Loaded by both Python infer_prefix_risk and
                        Android calculatePrefixRisk so train and runtime see the
                        same prefix risk distribution.
  prefix_histogram.json
                      — 6-digit prefix → {block_share, warn_share, seen_count}
                        Phase 3: «3-D embedding» префикса вместо in-graph embedding.
  def_code_risk.json  — DEF-code → P(BLOCK | def_code) (3-digit, более широкий,
                        чем prefix_risk, для cold-start когда конкретный 6-знач
                        префикс не виден в трейне).
  operator_bucket.json — DEF-code → operator bucket (mts/megafon/beeline/tele2/mvno/other).
                        Используется FeatureExtractor.kt для cold-start, когда сырое
                        имя оператора недоступно (на устройстве его нет).

Sources:
  datasets/ru/processed/ru_numbers_labeled.csv   (canonical labels, used by prefix_risk)
  datasets/ru/processed/ru_metadata_features.csv (rich metadata: operator/def_code)

Smoothing for binary BLOCK rates:
  P(BLOCK|p) = (n_block + α·prior) / (n_total + α)
  with α=10, prior=overall P(BLOCK). Buckets with n_total < min_samples fall
  back to the prior — too noisy.
"""
