def _def_code_from_number(number: str) -> str:
    digits = ''.join(c for c in (number or '') if c.isdigit())
    if len(digits) >= 4 and digits[0] in ('7', '8'):
        return digits[1:4]
    if len(digits) >= 3:
        return digits[:3]
    return ''


def _shannon_entropy_normalised(counts: Dict[str, int]) -> float:
    """Shannon-энтропия по {ALLOW, WARN, BLOCK}, нормализована к [0..1] (max=log2(3)).

    Используется в prefixEntropy: высокая энтропия = смешанный префикс (часть BLOCK,
    часть ALLOW) → сигнал осторожности (часто WARN). Низкая = «чистый» класс.
    """
    total = sum(counts.values())
    if total <= 0:
        return 0.0
    h = 0.0
    for label in ('ALLOW', 'BLOCK', 'WARN'):
        n = counts.get(label, 0)
        if n <= 0:
            continue
        p = n / total
        h -= p * math.log2(p)
    max_h = math.log2(3)
    if max_h <= 0:
        return 0.0
    return round(min(1.0, max(0.0, h / max_h)), 4)


def compute_prefix_histogram(
    rows: List[Dict[str, str]],
    prefix_length: int = 6,
    min_samples: int = 3,
    include_entropy: bool = True,
) -> Dict:
    """Prefix histogram с настраиваемым prefix_length: P(BLOCK), P(WARN), seen_count + entropy.

    `prefix_length` — char-длина включая `+7`. 5 = 3 телефонных цифры (def_code),
    6 = 4 (дефолтный Phase 3), 9 = 7 (самый точный).

    Это «3-D embedding» префикса: модель получает явный сигнал поведения номеров
    из той же серии. Cold-start друг: даже если конкретный номер не в трейне,
    его префикс с высокой долей BLOCK поднимет вердикт.
    """
    n_total = sum(1 for r in rows if r.get('метка') in ('ALLOW', 'BLOCK', 'WARN'))
    n_block = sum(1 for r in rows if r.get('метка') == 'BLOCK')
    n_warn = sum(1 for r in rows if r.get('метка') == 'WARN')
    prior_block = n_block / max(n_total, 1)
    prior_warn = n_warn / max(n_total, 1)
    alpha = 10.0

    by_prefix: Dict[str, Counter] = defaultdict(Counter)
    for r in rows:
        n = r.get('номер') or ''
        lbl = r.get('метка')
        if not n.startswith('+7') or lbl not in ('ALLOW', 'BLOCK', 'WARN'):
            continue
        p = n[:prefix_length]
        by_prefix[p][lbl] += 1
        by_prefix[p]['TOTAL'] += 1

    out: Dict[str, Dict] = {}
    for p, c in by_prefix.items():
        total = c.get('TOTAL', 0)
        if total < min_samples:
            continue
        block_share = (c.get('BLOCK', 0) + alpha * prior_block) / (total + alpha)
        warn_share = (c.get('WARN', 0) + alpha * prior_warn) / (total + alpha)
        entry: Dict = {
            'block_share': round(min(1.0, max(0.0, block_share)), 4),
            'warn_share': round(min(1.0, max(0.0, warn_share)), 4),
            'seen_count': total,
        }
        if include_entropy:
            entry['entropy'] = _shannon_entropy_normalised({
                'ALLOW': c.get('ALLOW', 0),
                'BLOCK': c.get('BLOCK', 0),
                'WARN': c.get('WARN', 0),
            })
        out[p] = entry

    max_seen = max((v['seen_count'] for v in out.values()), default=1)
    seen_log_norm = math.log1p(max_seen)
    return {
        'version': datetime.now().strftime('%Y%m%d-%H%M%S'),
        'created_at': datetime.now().isoformat(),
        'source_rows': n_total,
        'overall_block_rate': round(prior_block, 4),
        'overall_warn_rate': round(prior_warn, 4),
        'min_samples': min_samples,
        'smoothing_alpha': alpha,
        'prefix_length': prefix_length,
        'seen_log_norm': round(seen_log_norm, 4),
        'sample_size_saturation': PREFIX_SAMPLE_SIZE_SATURATION,
        'prefixes': dict(sorted(out.items())),
    }


def compute_def_code_risk(rows: List[Dict[str, str]], min_samples: int = 5) -> Dict:
    """3-digit DEF-code → P(BLOCK | def_code), сглажено."""
    n_total = sum(1 for r in rows if r.get('метка') in ('ALLOW', 'BLOCK', 'WARN'))
    n_block = sum(1 for r in rows if r.get('метка') == 'BLOCK')
    prior = n_block / max(n_total, 1)
    alpha = 20.0

    by_def: Dict[str, Dict[str, int]] = defaultdict(lambda: {'BLOCK': 0, 'TOTAL': 0})
    for r in rows:
        n = r.get('номер') or ''
        lbl = r.get('метка')
        if not n.startswith('+7') or lbl not in ('ALLOW', 'BLOCK', 'WARN'):
            continue
        def_code = _def_code_from_number(n)
        if not def_code:
            continue
        by_def[def_code]['TOTAL'] += 1
        if lbl == 'BLOCK':
            by_def[def_code]['BLOCK'] += 1

    out: Dict[str, float] = {}
    for d, c in by_def.items():
        total = c['TOTAL']
        if total < min_samples:
            continue
        risk = (c['BLOCK'] + alpha * prior) / (total + alpha)
        out[d] = round(min(1.0, max(0.0, risk)), 4)

    return {
        'version': datetime.now().strftime('%Y%m%d-%H%M%S'),
        'created_at': datetime.now().isoformat(),
        'source_rows': n_total,
        'overall_block_rate': round(prior, 4),
        'fallback_risk': round(prior, 4),
        'min_samples': min_samples,
        'smoothing_alpha': alpha,
        'def_codes': dict(sorted(out.items())),
    }


def compute_def_code_operator_risk(
    rows: List[Dict[str, str]],
    metadata_rows: List[Dict[str, str]],
    min_samples: int = 5,
) -> Dict:
    """P(BLOCK | operator_bucket × def_code), сглажено.

    Cross-фича Phase 4B: один и тот же DEF-код может иметь разную долю BLOCK
    в зависимости от оператора (MNO vs MVNO, MTS vs Megafon). Лукап получает
    bucket из shipped operator_bucket.json (вызов operator_bucket() c операторской
    строкой из metadata) и def_code из номера.
    """
    # Build number -> operator_bucket map from metadata.
    num_to_bucket: Dict[str, str] = {}
    for r in metadata_rows:
        n = r.get('номер') or ''
        op = r.get('оператор') or ''
        if not n.startswith('+7') or not op:
            continue
        bucket = operator_bucket(op)
        if bucket == 'unknown':
            continue
        num_to_bucket[n] = bucket

    n_total = sum(1 for r in rows if r.get('метка') in ('ALLOW', 'BLOCK', 'WARN'))
    n_block = sum(1 for r in rows if r.get('метка') == 'BLOCK')
    prior = n_block / max(n_total, 1)
    alpha = 20.0

    by_pair: Dict[str, Dict[str, Dict[str, int]]] = defaultdict(lambda: defaultdict(lambda: {'BLOCK': 0, 'TOTAL': 0}))
    for r in rows:
        n = r.get('номер') or ''
        lbl = r.get('метка')
        if not n.startswith('+7') or lbl not in ('ALLOW', 'BLOCK', 'WARN'):
            continue
        bucket = num_to_bucket.get(n)
        if not bucket:
            continue
        def_code = _def_code_from_number(n)
        if not def_code:
            continue
        cell = by_pair[bucket][def_code]
        cell['TOTAL'] += 1
        if lbl == 'BLOCK':
            cell['BLOCK'] += 1

    out_buckets: Dict[str, Dict[str, float]] = {}
    n_pairs = 0
    for bucket, by_def in by_pair.items():
        bucket_out: Dict[str, float] = {}
        for def_code, cell in by_def.items():
            total = cell['TOTAL']
            if total < min_samples:
                continue
            risk = (cell['BLOCK'] + alpha * prior) / (total + alpha)
            bucket_out[def_code] = round(min(1.0, max(0.0, risk)), 4)
            n_pairs += 1
        if bucket_out:
            out_buckets[bucket] = dict(sorted(bucket_out.items()))

    return {
        'version': datetime.now().strftime('%Y%m%d-%H%M%S'),
        'created_at': datetime.now().isoformat(),
        'source_rows': n_total,
        'overall_block_rate': round(prior, 4),
        'fallback_risk': round(prior, 4),
        'min_samples': min_samples,
        'smoothing_alpha': alpha,
        'pairs': n_pairs,
        'buckets': out_buckets,
    }


def compute_operator_bucket_lookup(metadata_rows: List[Dict[str, str]], min_samples: int = 3) -> Dict:
    """DEF-code → mode operator_bucket из metadata (operator string).

    На устройстве у нас НЕТ сырой строки оператора (нет open API). Поэтому
    при инференсе для cold-start мы определяем оператор по DEF-коду через этот
    лукап. Точно так же, как numbering plan, но bucketized.
    """
    by_def: Dict[str, Counter] = defaultdict(Counter)
    for r in metadata_rows:
        n = r.get('номер') or ''
        if not n.startswith('+7'):
            continue
        op = r.get('оператор') or ''
        if not op:
            continue
        bucket = operator_bucket(op)
        if bucket == 'unknown':
            continue
        def_code = _def_code_from_number(n)
        if not def_code:
            continue
        by_def[def_code][bucket] += 1

    out: Dict[str, str] = {}
    for d, counter in by_def.items():
        total = sum(counter.values())
        if total < min_samples:
            continue
        bucket, count = counter.most_common(1)[0]
        # Применяем majority vote: бакет должен быть доминирующим (>50%).
        if count / total < 0.5:
            continue
        out[d] = bucket

    return {
        'version': datetime.now().strftime('%Y%m%d-%H%M%S'),
        'created_at': datetime.now().isoformat(),
        'source_metadata_rows': len(metadata_rows),
        'min_samples': min_samples,
        'buckets': ['mts', 'megafon', 'beeline', 'tele2', 'mvno', 'other'],
        'def_codes': dict(sorted(out.items())),
    }


def compute_operator_assets_from_metadata_csv(
    metadata_csv: str,
    min_operator_samples: int = 3,
    min_pair_samples: int = 5,
) -> Tuple[Dict, Dict]:
    """Build operator lookup and operator×DEF risk in one streaming pass.

    Large full-batch `ru_metadata_features.csv` files can exceed 1 GB. Loading
    them as `list(DictReader(...))` together with the labeled corpus can OOM on
    normal dev VMs, so the full-corpus path streams the metadata file.
    """
    by_def: Dict[str, Counter] = defaultdict(Counter)
    by_pair: Dict[str, Dict[str, Dict[str, int]]] = defaultdict(
        lambda: defaultdict(lambda: {'BLOCK': 0, 'TOTAL': 0})
    )
    label_counts: Counter = Counter()
    rows = 0

    with open(metadata_csv, encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows += 1
            number = row.get('номер') or row.get('normalized_number') or row.get('number') or ''
            label = row.get('метка') or row.get('label') or ''
            label_counts[label] += 1
            if not number.startswith('+7') or label not in ('ALLOW', 'BLOCK', 'WARN'):
                continue
            def_code = row.get('деф_код') or _def_code_from_number(number)
            if not def_code:
                continue
            bucket = operator_bucket(row.get('оператор') or row.get('operator') or '')
            if bucket == 'unknown':
                continue
            by_def[def_code][bucket] += 1
            cell = by_pair[bucket][def_code]
            cell['TOTAL'] += 1
            if label == 'BLOCK':
                cell['BLOCK'] += 1

    operator_out: Dict[str, str] = {}
    for def_code, counter in by_def.items():
        total = sum(counter.values())
        if total < min_operator_samples:
            continue
        bucket, count = counter.most_common(1)[0]
        if count / total >= 0.5:
            operator_out[def_code] = bucket

    n_total = sum(label_counts[k] for k in ('ALLOW', 'BLOCK', 'WARN'))
    n_block = label_counts['BLOCK']
    prior = n_block / max(n_total, 1)
    alpha = 20.0
    risk_buckets: Dict[str, Dict[str, float]] = {}
    n_pairs = 0
    for bucket, by_def_code in by_pair.items():
        bucket_out: Dict[str, float] = {}
        for def_code, cell in by_def_code.items():
            total = cell['TOTAL']
            if total < min_pair_samples:
                continue
            risk = (cell['BLOCK'] + alpha * prior) / (total + alpha)
            bucket_out[def_code] = round(min(1.0, max(0.0, risk)), 4)
            n_pairs += 1
        if bucket_out:
            risk_buckets[bucket] = dict(sorted(bucket_out.items()))

    operator_lookup = {
        'version': datetime.now().strftime('%Y%m%d-%H%M%S'),
        'created_at': datetime.now().isoformat(),
        'source_metadata_rows': rows,
        'min_samples': min_operator_samples,
        'buckets': ['mts', 'megafon', 'beeline', 'tele2', 'mvno', 'other'],
        'def_codes': dict(sorted(operator_out.items())),
    }
    def_operator_risk = {
        'version': datetime.now().strftime('%Y%m%d-%H%M%S'),
        'created_at': datetime.now().isoformat(),
        'source_rows': n_total,
        'source_metadata_rows': rows,
        'overall_block_rate': round(prior, 4),
        'fallback_risk': round(prior, 4),
        'min_samples': min_pair_samples,
        'smoothing_alpha': alpha,
        'pairs': n_pairs,
        'buckets': risk_buckets,
    }
    return operator_lookup, def_operator_risk


def main() -> int:
    if not os.path.isfile(LABELS_CSV):
        print(f'ERROR: missing {LABELS_CSV}', file=sys.stderr)
        return 1
    rows = load_labels()
    print(f'Loaded {len(rows)} labeled numbers from {LABELS_CSV}')

    n_block = write_spam_numbers(rows)
    print(f'Wrote {OUT_CSV} ({n_block} exact BLOCK numbers + {len(CURATED_PREFIXES)} prefixes)')

    prefix_risk = compute_prefix_risk(rows)
    with open(OUT_PREFIX_JSON, 'w', encoding='utf-8') as f:
        json.dump(prefix_risk, f, ensure_ascii=False, indent=2)
    print(f'Wrote {OUT_PREFIX_JSON} ({len(prefix_risk["prefixes"])} prefixes; '
          f'fallback={prefix_risk["fallback_risk"]:.3f}, '
          f'overall_block_rate={prefix_risk["overall_block_rate"]:.3f})')

    # Phase 3 lookups.
    prefix_hist = compute_prefix_histogram(rows, prefix_length=6)
    with open(OUT_PREFIX_HIST_JSON, 'w', encoding='utf-8') as f:
        json.dump(prefix_hist, f, ensure_ascii=False, indent=2)
    print(f'Wrote {OUT_PREFIX_HIST_JSON} ({len(prefix_hist["prefixes"])} prefixes; '
          f'seen_log_norm={prefix_hist["seen_log_norm"]:.2f}; entropy field included)')

    # Phase 4B: 3-digit (5-char) и 7-digit (9-char) prefix histograms для multi-resolution.
    prefix_hist3 = compute_prefix_histogram(rows, prefix_length=5, min_samples=3, include_entropy=False)
    with open(OUT_PREFIX_HIST3_JSON, 'w', encoding='utf-8') as f:
        json.dump(prefix_hist3, f, ensure_ascii=False, indent=2)
    print(f'Wrote {OUT_PREFIX_HIST3_JSON} ({len(prefix_hist3["prefixes"])} prefixes; '
          f'prefix_length={prefix_hist3["prefix_length"]} chars / 3 phone digits)')

    prefix_hist7 = compute_prefix_histogram(rows, prefix_length=9, min_samples=3, include_entropy=False)
    with open(OUT_PREFIX_HIST7_JSON, 'w', encoding='utf-8') as f:
        json.dump(prefix_hist7, f, ensure_ascii=False, indent=2)
    print(f'Wrote {OUT_PREFIX_HIST7_JSON} ({len(prefix_hist7["prefixes"])} prefixes; '
          f'prefix_length={prefix_hist7["prefix_length"]} chars / 7 phone digits)')

    def_code_risk = compute_def_code_risk(rows)
    with open(OUT_DEF_CODE_RISK_JSON, 'w', encoding='utf-8') as f:
        json.dump(def_code_risk, f, ensure_ascii=False, indent=2)
    print(f'Wrote {OUT_DEF_CODE_RISK_JSON} ({len(def_code_risk["def_codes"])} DEF-codes; '
          f'fallback={def_code_risk["fallback_risk"]:.3f})')

    if os.path.isfile(METADATA_CSV):
        # Full-batch metadata can be >1 GB, so build these two assets in a
        # streaming pass instead of materialising all metadata rows in memory.
        op_bucket, def_op_risk = compute_operator_assets_from_metadata_csv(METADATA_CSV)
        with open(OUT_OPERATOR_BUCKET_JSON, 'w', encoding='utf-8') as f:
            json.dump(op_bucket, f, ensure_ascii=False, indent=2)
